#ifndef SCREEN_QUAD_H__
#define SCREEN_QUAD_H__
#include <ngl/Vec2.h>
#include <ngl/Vec3.h>
#include <string>
class ScreenQuad
{
  public :
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor, note this will scale to any screen size so
    /// @param _shader the name of the shader to use
    //----------------------------------------------------------------------------------------------------------------------
    ScreenQuad(const std::string &_shader );
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief dtor will clear the VAO
    //----------------------------------------------------------------------------------------------------------------------
    ~ScreenQuad();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief draw the scene
    //----------------------------------------------------------------------------------------------------------------------
    void draw() const;

  private :
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief VAO for screen quad
    //----------------------------------------------------------------------------------------------------------------------
    GLuint m_vao;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief the name of the shader to use when drawing
    //----------------------------------------------------------------------------------------------------------------------
    std::string m_shader;

};




#endif
